const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const personalDetailSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  dateOfBirth: {
    type: Date,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  }
});

const PersonalDetail = mongoose.model("PersonalDetail", personalDetailSchema);

module.exports = PersonalDetail;
